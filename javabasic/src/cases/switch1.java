package cases;

public class switch1 {

	public static void main(String[] args) {
		int month=5;
		switch(month) {
		case 1:
			System.out.println("month is january");
			break;
		case 3:
			System.out.println("month is march");
			break;
		case 4:
			System.out.println("month is april");
			break;
			default:
				System.out.println("month is not present");
				break;
			
		}

	}

}
